    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'guest_name' => 'required',
            'guest_email' => 'required',
            'comment' => 'required'

        ]);

        //Create Post
        $comment = new Comment;
        $comment->guest_name = $request->post('guest_name');
        $comment->guest_email = $request->post('guest_email');
        $comment->guest_website = $request->post('guest_website');
        $comment->comment = $request->post('comment');
        $comment->post_id = $request->post('post_id');
        $comment->approved = '0';

        $comment->save();

        return json_encode(['status' => 'OK','data' => 'Comment Successful!']);
    }

