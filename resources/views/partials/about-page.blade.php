                        <h2 class="mb-5">Our Programs</h2>

                        <div class="container">
                            <section class="section-content" id="schoolforall">
                                <h5>SCHOOL FOR ALL</h5>
                                <p>
                                    A coordinated annual campaign aimed at raising and paying tuition fees for at least
                                    1000 less privileged youths (high school and University students) every September
                                    and providing free learning materials such as books, calculators, computers and
                                    learning tablets to them.
                                </p>
                                <h5>Educative Series</h5>
                                <p>
                                    This has to do with chains of relevant activities that directly provide value to
                                    young people. Example YOUTAF’s Monthly Webinars.
                                </p>
                            </section>
                            <section class="section-content" id="youtafxp">
                                <h5>YOUTAF XP</h5>
                                <p>
                                    <strong>YOUTAF exchange program (YOUTAF-XP)</strong> will enable us interchange
                                    volunteers from our different communities (nationally) during summer every year to
                                    render community service in cities across Canada. Our YOUTAF-XP is aimed at
                                    preparing young for big leadership roles in future and also bridging the gap of
                                    cultural diversity. The learning program is based on:
                                </p>
                                <p class="my-3">
                                    <ul>
                                        <li>
                                            Community Service, Participants must relocate to other cities, which will
                                            enable them to learn new culture, language and get to know more about the
                                            country.
                                        </li>
                                        <li>
                                            Practical, Learning on a job experience through paid, full time training.
                                        </li>
                                        <li>
                                            Leadership training and career development
                                        </li>
                                        <li>
                                            The building of communities through participating in and addressing
                                            community needs.
                                        </li>
                                    </ul>
                                </p>
                                <p>
                                    Interchanged volunteers (Talented Fellows) get their relocation cost paid for, free
                                    accommodation and free feeding. They will be placed with non-profit organizations
                                    where they assist to address critical community needs such as youth development,
                                    education, workforce development, environmental issues, arts, and community health.
                                    YOUTAF-XP is a great way for youths to emerge as leaders and to build their capacity
                                    of community organization.
                                </p>
                            </section>
                            <section class="section-content" id="youtafhub">
                                <h5>YOUTAF HUB</h5>
                                <p>
                                    A creative working space in different communities where members meet on weekends to
                                    execute academic and community projects together. The main goal is to promote
                                    communication, teamwork and exposure. We feature guests who are influential,
                                    innovative and successful in their respective fields every weekend to talk about
                                    random topics affecting the youths in the society such as Youths in Politics,
                                    Sciences, Environmental issues, public Speaking and community development.
                                </p>
                            </section>
                            <section class="section-content" id="special_aid">
                                <h5>YOUTAF Special Aid</h5>
                                <p>
                                    The idea here is for our members to give back to the communities wherever they find
                                    themselves continually. We plan to partner with other NGO’s like ‘Medecins Sans
                                    Frontiers’ etc. to provide non-skilled assistance during their outreaches. We are
                                    keen to partnerships; in such we can collaboratively support the community. </p>
                            </section>
                            <section class="section-content" id="webinar">
                                <h5>YOUTAF WEBINAR</h5>
                                <p>
                                    This is an online seminar, where we invite mentors or experts on any selected field.
                                    The invited guests speak and share on relevant/trending topics via Instagram or
                                    Zoom. Our leverage will be the reputation of the invited guests. We could use the
                                    relationships we have already established to pull participants from all over the
                                    globe.</p>
                            </section>
                            <section class="section-content" id="competition">
                                <h5>YOUTAF Creative Competitions</h5>
                                <p>
                                    We will organize creative competitions on selected themes and receive entries from
                                    all around the world on Essay Writing, Graphic Design, Photography and Fine art.
                                    This is a very easy way to announcing our name to students in various universities
                                    around the world. Winners will be presented with motivating cash prizes and
                                    exclusive gifts from sponsors.</p>
                            </section>
